// Function to check if a code is valid and return the prize type
export function checkPromoCode(code: string): {
  valid: boolean
  prizeType?: "peanutButter" | "airtime" | "data" | "luxury" | "betterLuck"
  airtimeAmount?: number
  message?: string
} {
  const upperCode = code.toUpperCase()

  // Check for Grand Prize (luxury hotel stay)
  if (luxuryWinCodes.includes(upperCode)) {
    const isEaster = upperCode === "GDA1B2C3"
    return {
      valid: true,
      prizeType: "luxury",
      message: isEaster
        ? "CONGRATULATIONS! You've won the GRAND PRIZE - a luxury stay at Mövenpick Hotel for Easter!"
        : "CONGRATULATIONS! You've won the GRAND PRIZE - a luxury stay at Mövenpick Hotel for Christmas!",
    }
  }

  // Check for free peanut butter
  if (freePeanutButterCodes.includes(upperCode) || upperCode.startsWith("PB")) {
    return { valid: true, prizeType: "peanutButter" }
  }

  // Check for airtime (original codes)
  if (airtimeCodes.includes(upperCode)) {
    let amount = 5
    if (upperCode.includes("10")) amount = 10
    if (upperCode.includes("20")) amount = 20

    return { valid: true, prizeType: "airtime", airtimeAmount: amount }
  }

  // Check for AIR20 codes
  if (airtime20Codes.includes(upperCode) || upperCode.startsWith("AR") || upperCode.startsWith("AIR20")) {
    return { valid: true, prizeType: "airtime", airtimeAmount: 20 }
  }

  // Check for AIR10 codes
  if (airtime10Codes.includes(upperCode) || upperCode.startsWith("MB") || upperCode.startsWith("AIR10")) {
    return { valid: true, prizeType: "airtime", airtimeAmount: 10 }
  }

  // Check for DATA10 codes
  if (data10Codes.includes(upperCode) || upperCode.startsWith("DATA10")) {
    return { valid: true, prizeType: "data", airtimeAmount: 10 }
  }

  // Check for Better Luck codes
  if (betterLuckCodes.includes(upperCode) || upperCode.startsWith("TRYNEXT") || upperCode.startsWith("NEXTTIME")) {
    return {
      valid: true,
      prizeType: "betterLuck",
      message: "Better luck next time! Keep trying with more Stilla Peanut Butter purchases.",
    }
  }

  // Not a valid code
  return { valid: false }
}

